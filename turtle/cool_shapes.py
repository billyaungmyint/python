import turtle

#circle draws circle
turtle.shape("circle")
turtle.circle(40)

#square draws square
turtle.shape("square")
turtle.forward(80)
turtle.right(90)
turtle.forward(80)
turtle.right(90)
turtle.forward(80)
turtle.right(90)
turtle.forward(80)
turtle.left(90)

#triangle draws triangle

turtle.shape("triangle")
turtle.forward(120)
turtle.left(135)
turtle.forward(170)
turtle.left(135)
turtle.forward(120)

#final spin
turtle.right(720)  
